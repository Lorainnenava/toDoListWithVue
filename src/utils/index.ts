/**
 * Transforma una cadena de texto en mayúsculas.
 * @param value
 * @returns String
 */
export const upperCase = (value: string) => `${value || ''}`.toUpperCase()

/**
 * Transforma una cadena de texto en minúsculas.
 * @param value
 * @returns String
 */
export const lowercase = (value: string) => `${value || ''}`.toLowerCase()
